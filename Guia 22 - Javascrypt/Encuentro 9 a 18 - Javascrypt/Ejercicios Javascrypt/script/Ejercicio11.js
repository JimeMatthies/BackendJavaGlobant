/* Actividad Condicionales: If */

let nota = parseInt(prompt("¿Cual es tu calificación?"));

if (nota >= 70) {
	console.log("Nota: " + nota);
	console.log("Felicidades, ¡aprobaste el examen!");
} else {
	console.log("Nota: " + nota);
	console.log("Lo siento, no aprobaste el examen. ¡Sigue estudiando!");
}
